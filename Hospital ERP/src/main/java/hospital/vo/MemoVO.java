package hospital.vo;

public class MemoVO {
	private int m_num, m_e_code;
	private String m_date, m_memo;
	
	public int getM_num() {
		return m_num;
	}
	public void setM_num(int m_num) {
		this.m_num = m_num;
	}
	public int getM_e_code() {
		return m_e_code;
	}
	public void setM_e_code(int m_e_code) {
		this.m_e_code = m_e_code;
	}
	public String getM_date() {
		return m_date;
	}
	public void setM_date(String m_date) {
		this.m_date = m_date;
	}
	public String getM_memo() {
		return m_memo;
	}
	public void setM_memo(String m_memo) {
		this.m_memo = m_memo;
	}
	
}
